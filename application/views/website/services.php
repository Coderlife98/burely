                <p class="text-justify">
                    Brewing masala tea involves a few simple steps. First, bring water to a boil in a pot. Then, add
                    black tea leaves along with crushed or whole spices like cardamom, cinnamon, ginger, and cloves.
                    Allow the mixture to simmer for a few minutes to infuse the flavors into the water. Next, add
                    milk to the pot and let it simmer for a bit longer, stirring occasionally. Finally, sweeten the
                    tea with sugar or your preferred sweetener according to taste. Once the tea reaches your desired
                    strength and flavor, strain it into cups and serve hot. Adjust the proportions of tea leaves,
